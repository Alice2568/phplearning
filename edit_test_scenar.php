### Scénario de RPG : À l'ombre des Runes

#### Contexte
Vous êtes un aventurier dans un royaume ancien, "Eldoria", où magie et mystère dominent. Récemment, des événements surnaturels ont perturbé les habitants. Un étrange artefact runique a été découvert dans les ruines d'une ancienne cité. Vous êtes chargé d'enquêter sur cet objet et de découvrir sa véritable nature.

---

### Introduction
Vous êtes convoqué par le Conseil des Sages d'Eldoria. Dans une salle faiblement éclairée, ornée de runes étincelantes, un mage vous tend un fragment de pierre gravé.

**Mage Aelric** : "Ce fragment est empreint d'une magie ancienne. Il nous met en garde contre un danger imminent. Mais seul un aventurier courageux pourra découvrir la vérité. Acceptez-vous cette mission ?"

1. **Accepter la mission.**
2. **Refuser et poser des questions sur le danger.**
3. **Exiger une récompense avant de poursuivre.**

---

### Premier choix : Départ vers les ruines
**Si vous acceptez la mission :**
Le mage vous donne une carte et un cristal d'identification magique. Vous partez immédiatement pour les ruines dans une forêt dense.

**Si vous refusez ou hésitez :**
Le mage révèle que les visions liées à l'artefact prédisent un cataclysme. Vous avez finalement le choix entre partir ou être remplacé par un autre aventurier (ce qui vous disqualifie de la mission principale).

**Si vous exigez une récompense :**
Le Conseil accepte à contrecœur et promet une bourse d'or et une relique magique si vous réussissez. Vous partez avec un certain ressentiment envers les sages.

---

### Exploration des ruines
Vous arrivez dans les ruines et êtes confronté à une bifurcation :

1. **Prendre le tunnel sombre à gauche, où un faible écho résonne.**
2. **Explorer la grande salle à droite, illuminée par une lueur mystérieuse.**
3. **Chercher des indices dans les débris alentours avant de choisir une direction.**

**Résultats des choix :**
- **Tunnel sombre :** Vous trouvez une créature spectral à affronter ou à apaiser. Une rune cachée dans le mur vous offre un indice crucial sur l'artefact.
- **Grande salle :** Une illusion vous piège temporairement, et vous devez résoudre une énigme magique pour avancer.
- **Recherche d'indices :** Vous découvrez un journal d'un ancien mage qui révèle des informations clés sur les dangers à venir, mais perdez un temps précieux.

---

### Rencontre avec l'artefact
En étudiant les runes, vous activez l'artefact, provoquant une apparition.

**Esprit des Runes** : "Mortel, ton destin est lié au mien. Choisis sagement."

1. **Accepter de fusionner avec la puissance des runes.**
2. **Tenter de détruire l'artefact pour éviter le danger.**
3. **Negocier pour obtenir des informations sur son pouvoir.**

**Conséquences :**
- **Fusion :** Vous gagnez des pouvoirs magiques mais devenez la cible d'une secte ancienne.
- **Destruction :** Vous déclenchez une onde magique, sauvant le royaume mais causant des dommages collatéraux. Les sages vous blâment.
- **Négociation :** Vous apprenez un secret qui vous permet de sceller l'artefact, mais au prix de votre propre énergie vitale.

---

### Conclusion
Vos choix influencent la fin : le royaume prospère ou sombre dans le chaos, et votre nom entre dans la légende... ou l'oubli.

---

Ce scénario peut être développé avec des détails supplémentaires sur les énigmes, les ennemis, et les conséquences morales de vos choix. Prêt pour l'aventure ?

